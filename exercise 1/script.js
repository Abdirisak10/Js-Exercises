// The Arthematic Operations Exercise , the first Exercise

let x=7;
let y=3;

console.log(x+y);
console.log(x-y);
console.log(x*y);
console.log(x/y);

// The modulus 
console.log(x % y);

// Increment
// 1 - pre-increment
console.log(++y);
// 2- post increment
console.log(y++);
console.log(y);


// Decrement
// 1- pre decrement
console.log(--x);
// Post Decrement
console.log(x--);
console.log(x);
